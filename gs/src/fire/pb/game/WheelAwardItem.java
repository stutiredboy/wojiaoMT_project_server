package fire.pb.game;

public class WheelAwardItem {
	public final int itemid;
	public final int itemnum;
	public final int notice;
	public final int msg;
	public final int mustitem;
	public final int mustnum;
	
	public WheelAwardItem(final int itemid, final int itemnum, final int notice, final int msg, final int mustitem, final int mustnum) {
		this.itemid = itemid;
		this.itemnum = itemnum;
		this.notice = notice;
		this.msg = msg;
		this.mustitem = mustitem;
		this.mustnum = mustnum;
	}

}
