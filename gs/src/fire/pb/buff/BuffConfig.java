package fire.pb.buff;

public class BuffConfig
{
	protected int buffTypeId;	
	protected String className;	
	protected int flagType;

	public int getBuffTypeId(){return buffTypeId;}
	public void setBuffTypeId(int buffTypeId){this.buffTypeId = buffTypeId;}
	public String getClassName(){return className;}
	public void setClassName(String className){	this.className = className;	}
	public int getBuffFlagType(){return flagType;}
	public void setBuffFlagtype(int flagType){this.flagType = flagType;}	
	
	
}
